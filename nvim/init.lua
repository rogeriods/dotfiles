-- [[ Basic Keymaps ]]
-- Open file explorer.
vim.keymap.set('n', '<leader>pv', vim.cmd.Ex, { desc = 'File Explorer' })

-- Move selected lines up and down.
vim.keymap.set('v', 'J', ":m '>+1<CR>gv=gv")
